.MODEL SMALL
.STACK 100H
.DATA
string1 DB 10,13,"LARGER $"
string2 DB 10,13,"SMALLER $"
.CODE
MAIN PROC
 MOV CX,@DATA
 MOV DS,CX

 MOV AH,1
 INT 21H
 MOV BL,AL

 MOV AH,2
 MOV DL,10
 INT 21H
 MOV DL,13
 INT 21H

 MOV AH,1
 INT 21H
 MOV CL,AL

 CMP BL,CL
 JG FIRSTLEVEL
 JMP SecondLevel

 FIRSTLEVEL:
 MOV AH,9
 LEA DX,string1
 INT 21H

 MOV AH,2
 MOV DL,BL
 INT 21H

 MOV AH,9
 LEA DX,string2
 INT 21H

 MOV AH,2
 MOV DL,CL
 INT 21H

 JMP EXIT

 SecondLevel:
 MOV AH,9
 LEA DX,string1
 INT 21H

 MOV AH,2
 MOV DL,CL
 INT 21H

 MOV AH,9
 LEA DX,string2
 INT 21H

 MOV AH,2
 MOV DL,BL
 INT 21H

 EXIT:
 MOV AH,4CH
 INT 21H
 ENDP MAIN 
